const findMatchFlowTextId = (progress, words = []) => {
  const filterWords = words.filter((w) => w.s < w.t);
  if (!filterWords.length) return '';
  let left = 0;
  let right = filterWords.length - 1;
  while (left <= right) {
    const mid = Math.floor((left + right) / 2);
    const midWord = filterWords[mid];
    if (midWord) {
      if (midWord.s <= progress && midWord.t >= progress) {
        return midWord;
      }
      if (midWord.t < progress) {
        left = mid + 1;
      } else {
        right = mid - 1;
      }
    }
  }
};

